             '*_mask-temporal-lobe.nii', ...
             '*_mask-subcortical-tissue.nii', ...
        }} ...
    );

    % --- Group 4: 皮質マスク (mask/roisフォルダ) ---
    job_groups{end+1} = struct(...
        'folder',   'mask/rois', ...
