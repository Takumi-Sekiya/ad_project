%%頭部MRI画像前処理パイプライン
%
%FreeSurferによる出力ファイルを用いてSPMによるVBM, 標準化を実施する
%
%(1)パラメータ設定　:解析に必要な情報の設定を行う
%(2)処理の選択　　　:実行するステップのフラグをtrueにする
%(3)パイプライン実行:スクリプトを実行する

clear;
spm('Defaults', 'fmri');
spm_jobman('initcfg');

%%(1)パラメータ設定
%---------------------------------------------------------------
%--- 基本パス設定 ---
project_dir = '/media/sf_ad_project/data'; %プロジェクトフォルダへのパス
